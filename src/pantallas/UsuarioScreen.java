    private void btnInscribirGimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInscribirGimActionPerformed
        // Verificar si usuarioActual está correctamente inicializado
    if (usuarioActual == null) {
        JOptionPane.showMessageDialog(this, "Error: El usuario no está correctamente inicializado.");
        return; // Salir del método si usuarioActual es null
    }

    // Verificar si el cliente ya está registrado en algún gimnasio
    for (GimnasioModel gimnasio : gimnasioController.obtenerGimnasios()) {
        if (gimnasio.getClientes_gimnasio().contains(usuarioActual.getNombre())) {
            JOptionPane.showMessageDialog(this, "Ya estás registrado en el gimnasio " + gimnasio.getNom_gym() + ".");
            return; // Salir del método si ya está registrado
        }
    }

    // Obtener el índice de la fila seleccionada en la tabla
    int filaSeleccionada = jTableDatosGimnasios.getSelectedRow();

    // Verificar si se seleccionó alguna fila
    if (filaSeleccionada != -1) {
        // Obtener el nombre del gimnasio seleccionado en esa fila
        String nombreGym = (String) jTableDatosGimnasios.getValueAt(filaSeleccionada, 0);

        // Guardar la inscripción del cliente en el gimnasio
        gimnasioController.guardarInscripcionGimnasio(nombreGym, usuarioActual.getNombre());

        // Mostrar mensaje de éxito
        JOptionPane.showMessageDialog(this, "¡Inscripción exitosa en " + nombreGym + "!");
    } else {
        JOptionPane.showMessageDialog(this, "Error: Debes seleccionar un gimnasio.");
    }
    }//GEN-LAST:event_btnInscribirGimActionPerformed
    
