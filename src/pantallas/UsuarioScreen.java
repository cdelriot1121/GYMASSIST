    private void cargarDatosGimnasios(GimnasioController gimnasioController) { // Pasar gimnasioController como parámetro
        // Obtener la lista de gimnasios del controlador
    List<GimnasioModel> gimnasios = gimnasioController.obtenerGimnasios();

    // Agregar mensajes de depuración
    System.out.println("Total de gimnasios cargados: " + gimnasios.size());
    for (GimnasioModel gimnasio : gimnasios) {
        System.out.println("Gimnasio cargado: " + gimnasio);
    }

        // Obtener la lista de gimnasios del controlador
        

        // Crear un modelo de tabla
        DefaultTableModel modeloTabla = new DefaultTableModel();
        modeloTabla.addColumn("Nombre Gim");
        modeloTabla.addColumn("Mensualidad");

        // Agregar los datos de los gimnasios al modelo de tabla
        for (GimnasioModel gimnasio : gimnasios) {
            Object[] fila = {gimnasio.getNom_gym(), gimnasio.getVal_mensual()};
            modeloTabla.addRow(fila);
        }

        // Asignar el modelo de tabla a la tabla jTableDatosGimnasios
        jTableDatosGimnasios.setModel(modeloTabla);
    }
    
    
    
    
